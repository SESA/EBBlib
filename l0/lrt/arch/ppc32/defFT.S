#include <arch/powerpc/asdef.h>
	
.section ".text"
.align 2
.globl defaultFuncCommon
.type defaultFuncCommon, @function
defaultFuncCommon:
	mflr r0
	stw r0, 0x4(r1)  //save link register
	stfd	f1,((-1)*8)(r1)			// save parameter fprs
	stfd	f2,((-2)*8)(r1)	
	stfd	f3,((-3)*8)(r1)
	stfd	f4,((-4)*8)(r1)
	stfd	f5,((-5)*8)(r1)
	stfd	f6,((-6)*8)(r1)
	stfd	f7,((-7)*8)(r1)
	stfd	f8,((-8)*8)(r1)
	stw r3, ((-64)+(-1)*4)(r1)              // save parameter gprs
	stw r4, ((-64)+(-2)*4)(r1)
	stw r5, ((-64)+(-3)*4)(r1)
	stw r6, ((-64)+(-4)*4)(r1)
	stw r7, ((-64)+(-5)*4)(r1)
	stw r8, ((-64)+(-6)*4)(r1)
	stw r9, ((-64)+(-7)*4)(r1)
	stw r10, ((-64)+(-8)*4)(r1)
	la r3, ((-64)+(-1)*4)(r1) //first param: &this	
	mr r4, r11                //second param: method number
	stwu r1, -(8+(8*8)+(8*4))(r1) //push frame
	bl GenericDefaultFunc //call generic function
	la r1, (8+(8*8)+(8*4))(r1)    //pop frame
	lwz r0, 0x4(r1)               //restore link register
	mtlr r0
	cmpi 0, 1, r3, 0                   //check return value
	bne   OnSuccess
	blr
OnSuccess:
	mtctr r3  //load ctr to branch to later
	lfd	f1,((-1)*8)(r1)			// restore parameter fprs
	lfd	f2,((-2)*8)(r1)	
	lfd	f3,((-3)*8)(r1)
	lfd	f4,((-4)*8)(r1)
	lfd	f5,((-5)*8)(r1)
	lfd	f6,((-6)*8)(r1)
	lfd	f7,((-7)*8)(r1)
	lfd	f8,((-8)*8)(r1)
	lwz r3, ((-64)+(-1)*4)(r1)              // restore parameter gprs
	lwz r4, ((-64)+(-2)*4)(r1)
	lwz r5, ((-64)+(-3)*4)(r1)
	lwz r6, ((-64)+(-4)*4)(r1)
	lwz r7, ((-64)+(-5)*4)(r1)
	lwz r8, ((-64)+(-6)*4)(r1)
	lwz r9, ((-64)+(-7)*4)(r1)
	lwz r10, ((-64)+(-8)*4)(r1)
	bctr                          //branch to method

